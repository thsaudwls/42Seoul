int	ft_str_is_alpha(char *str)
{
	int	count;

	count = 0;
	while (*str != 0x00 && count == 0)
	{
		if ((*str >= 65 && *str <= 90) || (*str >= 97 && *str <= 122))
			str ++;
		else
		{
			count = 1;
			str ++;
		}
	}
	if (count == 0)
		return (1);
	else
		return (0);
}

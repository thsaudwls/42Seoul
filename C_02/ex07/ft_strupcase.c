char	*ft_strupcase(char *str)
{
	char	*tmp;

	tmp = str;
	while (*str != 0x00)
	{
		if (*str >= 'a' && *str <= 'z')
			*str = *str - 32;
		str ++;
	}
	str = tmp;
	return (str);
}

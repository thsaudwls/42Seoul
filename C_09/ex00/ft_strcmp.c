int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && !(s1[i] == 0x00 && s2[i] == 0x00))
		i ++;
	return (s1[i] - s2[i]);
}

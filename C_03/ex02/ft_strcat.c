char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	n;

	i = 0;
	while (dest[i] != '\0')
	{
		i ++;
	}
	n = i;
	while (src[i - n] != '\0')
	{
		dest[i] = src[i - n];
		i ++;
	}
	dest[i] = 0x00;
	return (dest);
}

int	ft_any(char **tab, int (*f)(char *))
{
	int	i;
	int	cnt;

	cnt = 0;
	i = 0;
	while (tab[i] != 0x00)
		i ++;
	while (cnt < i)
	{
		if (f(tab[cnt]) != 0)
			return (1);
		cnt ++;
	}
	return (0);
}
